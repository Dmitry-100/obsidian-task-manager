// =============================================================================
// Tailwind CSS Configuration (v3)
// =============================================================================
// Конфигурация Tailwind CSS для проекта с shadcn/ui.
//
// content — указывает где искать классы для генерации CSS
// theme — кастомизация дизайн-системы (цвета, размеры, шрифты)
// plugins — дополнительные плагины
//
// ВАЖНО: shadcn/ui использует CSS переменные для цветов, которые мы
// определили в index.css. Здесь мы говорим Tailwind использовать эти переменные.
// =============================================================================

/** @type {import('tailwindcss').Config} */
export default {
  // ---------------------------------------------------------------------------
  // Dark mode
  // ---------------------------------------------------------------------------
  // "class" — тёмная тема включается добавлением класса "dark" к <html>
  // Альтернатива: "media" — использует системные настройки (prefers-color-scheme)
  // ---------------------------------------------------------------------------
  darkMode: ["class"],

  // ---------------------------------------------------------------------------
  // Content — где искать Tailwind классы
  // ---------------------------------------------------------------------------
  // Tailwind сканирует эти файлы и генерирует CSS только для используемых классов.
  // Это называется "tree-shaking" — убирает неиспользуемый код.
  // ---------------------------------------------------------------------------
  content: [
    "./index.html",
    "./src/**/*.{js,ts,jsx,tsx}",
  ],

  // ---------------------------------------------------------------------------
  // Theme — кастомизация дизайн-системы
  // ---------------------------------------------------------------------------
  theme: {
    // container — настройки для .container класса
    container: {
      center: true,          // Центрировать контейнер
      padding: "2rem",       // Внутренние отступы
      screens: {
        "2xl": "1400px",     // Максимальная ширина на больших экранах
      },
    },

    // extend — расширяем стандартные значения Tailwind (не заменяем!)
    extend: {
      // -----------------------------------------------------------------------
      // Цвета из CSS переменных
      // -----------------------------------------------------------------------
      // hsl(var(--xxx)) — используем CSS переменные из index.css
      // Это позволяет легко менять тему меняя только переменные
      // -----------------------------------------------------------------------
      colors: {
        // Основные цвета
        border: "hsl(var(--border))",
        input: "hsl(var(--input))",
        ring: "hsl(var(--ring))",
        background: "hsl(var(--background))",
        foreground: "hsl(var(--foreground))",

        // Основной акцентный цвет (кнопки, ссылки)
        primary: {
          DEFAULT: "hsl(var(--primary))",
          foreground: "hsl(var(--primary-foreground))",
        },

        // Вторичный цвет (менее важные элементы)
        secondary: {
          DEFAULT: "hsl(var(--secondary))",
          foreground: "hsl(var(--secondary-foreground))",
        },

        // Деструктивные действия (удаление, ошибки)
        destructive: {
          DEFAULT: "hsl(var(--destructive))",
          foreground: "hsl(var(--destructive-foreground))",
        },

        // Приглушённый (второстепенный контент)
        muted: {
          DEFAULT: "hsl(var(--muted))",
          foreground: "hsl(var(--muted-foreground))",
        },

        // Акцент (hover состояния, выделение)
        accent: {
          DEFAULT: "hsl(var(--accent))",
          foreground: "hsl(var(--accent-foreground))",
        },

        // Popover (выпадающие меню)
        popover: {
          DEFAULT: "hsl(var(--popover))",
          foreground: "hsl(var(--popover-foreground))",
        },

        // Card (карточки)
        card: {
          DEFAULT: "hsl(var(--card))",
          foreground: "hsl(var(--card-foreground))",
        },
      },

      // -----------------------------------------------------------------------
      // Border radius из CSS переменной
      // -----------------------------------------------------------------------
      borderRadius: {
        lg: "var(--radius)",
        md: "calc(var(--radius) - 2px)",
        sm: "calc(var(--radius) - 4px)",
      },

      // -----------------------------------------------------------------------
      // Анимации для shadcn/ui компонентов
      // -----------------------------------------------------------------------
      keyframes: {
        "accordion-down": {
          from: { height: "0" },
          to: { height: "var(--radix-accordion-content-height)" },
        },
        "accordion-up": {
          from: { height: "var(--radix-accordion-content-height)" },
          to: { height: "0" },
        },
      },
      animation: {
        "accordion-down": "accordion-down 0.2s ease-out",
        "accordion-up": "accordion-up 0.2s ease-out",
      },
    },
  },

  // ---------------------------------------------------------------------------
  // Plugins — дополнительные плагины Tailwind
  // ---------------------------------------------------------------------------
  // tailwindcss-animate — добавляет классы анимаций (animate-in, fade-in, etc.)
  // Используется shadcn/ui для плавных переходов
  // ---------------------------------------------------------------------------
  plugins: [require("tailwindcss-animate")],
}
