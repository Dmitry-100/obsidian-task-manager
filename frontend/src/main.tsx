// =============================================================================
// main.tsx — Точка входа React приложения
// =============================================================================
// Этот файл — самый первый JavaScript/TypeScript код который запускается.
// Его задача:
//   1. Найти DOM элемент <div id="root"> в index.html
//   2. Создать React root (корень виртуального DOM)
//   3. Отрендерить наше приложение внутрь этого элемента
//
// Виртуальный DOM (Virtual DOM):
//   React не работает напрямую с браузерным DOM (он медленный).
//   Вместо этого React создаёт виртуальную копию DOM в памяти,
//   сравнивает изменения, и обновляет только то что изменилось.
// =============================================================================

// -----------------------------------------------------------------------------
// Импорты из React
// -----------------------------------------------------------------------------
import { StrictMode } from 'react';
// StrictMode — специальный компонент для разработки:
//   - Рендерит компоненты дважды для обнаружения side effects
//   - Предупреждает об устаревших API
//   - Включает дополнительные проверки
// В production режиме StrictMode не влияет на производительность

import { createRoot } from 'react-dom/client';
// createRoot — новый API React 18 для создания корня приложения
// (раньше использовался ReactDOM.render, но он устарел)

// -----------------------------------------------------------------------------
// Импорт стилей
// -----------------------------------------------------------------------------
import './index.css';
// Этот импорт подключает наш CSS файл с TailwindCSS директивами.
// Vite + PostCSS обработают этот файл и сгенерируют финальный CSS.

// -----------------------------------------------------------------------------
// Импорт корневого компонента
// -----------------------------------------------------------------------------
import App from './App.tsx';
// App — это корневой компонент нашего приложения.
// Всё остальное — страницы, компоненты, роутинг — будет внутри App.

// =============================================================================
// Создание и рендеринг приложения
// =============================================================================

// document.getElementById('root')! — находим элемент с id="root" из index.html
// ! — это TypeScript non-null assertion (мы уверены что элемент существует)

createRoot(document.getElementById('root')!).render(
  <StrictMode>
    <App />
  </StrictMode>,
);

// Результат:
//   React возьмёт контроль над <div id="root"></div>
//   и отрендерит внутрь него наш компонент App
